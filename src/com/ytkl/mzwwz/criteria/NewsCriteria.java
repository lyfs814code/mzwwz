package com.ytkl.mzwwz.criteria;

import com.ytkl.mzwwz.base.Criteria;

public class NewsCriteria extends Criteria {

	private static final long serialVersionUID = 1L;

	private int id;// id

	private String titleQue;// 标题

	private String newsTypeQry;// 新闻类型

	public NewsCriteria() {
	}

	public NewsCriteria(String newsTypeQry) {
		this.newsTypeQry = newsTypeQry;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTitleQue() {
		return titleQue;
	}

	public void setTitleQue(String titleQue) {
		this.titleQue = titleQue;
	}

	public String getNewsTypeQry() {
		return newsTypeQry;
	}

	public void setNewsTypeQry(String newsTypeQry) {
		this.newsTypeQry = newsTypeQry;
	}

}
